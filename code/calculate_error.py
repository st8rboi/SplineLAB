import numpy as np

def calculate_max_error(f, p, x: np.ndarray) -> float:
    """
    Вычисление максимальной абсолютной погрешности между функцией и её аппроксимацией.

    Args:
        f: исходная функция (вызываемая).
        p: аппроксимирующая функция (вызываемая).
        x: массив точек, в которых сравниваются значения f и p.

    Returns:
        Максимальное значение абсолютной ошибки |f(x) - p(x)| по массиву x.
    """
    return np.max(np.abs(f(x) - p(x)))


def calculate_mse(f, p, x: np.ndarray) -> float:
    """
    Вычисление среднеквадратичной ошибки (MSE) между функцией и её аппроксимацией.

    Args:
        f: исходная функция (вызываемая).
        p: аппроксимирующая функция (вызываемая).
        x: массив точек, в которых сравниваются значения f и p.

    Returns:
        Среднеквадратичная ошибка между f(x) и p(x) по массиву x.
    """
    return np.mean((f(x) - p(x))**2)
